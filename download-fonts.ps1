# Script to download ARK-ES fonts
# Visit https://fontesk.com/ark-es-typeface/ and click DOWNLOAD to get the ZIP file
# Then extract the font files to public/fonts/ark-es/

Write-Host "ARK-ES Font Download Instructions:" -ForegroundColor Yellow
Write-Host "1. Visit: https://fontesk.com/ark-es-typeface/" -ForegroundColor Cyan
Write-Host "2. Click the DOWNLOAD button" -ForegroundColor Cyan
Write-Host "3. Extract the ZIP file" -ForegroundColor Cyan
Write-Host "4. Copy the following font files to public/fonts/ark-es/:" -ForegroundColor Cyan
Write-Host "   - arkessolidregular.woff (or .woff2, .ttf)" -ForegroundColor White
Write-Host "   - arkessolidbold.woff (or .woff2, .ttf)" -ForegroundColor White
Write-Host ""
Write-Host "Alternatively, you can download from:" -ForegroundColor Yellow
Write-Host "https://usemodify.com/fonts/ark-typeface/" -ForegroundColor Cyan



